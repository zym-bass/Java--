package com.ujiuye.abstractdemo;

//The type AbstractClass must be an abstract class to define abstract methods
public abstract class AbstractClass {	
	// 定义出一个抽象方法
	// The abstract method eat in type AbstractClass can only be defined by an abstract class
	// 抽象方法只能存在于抽象类中
	public abstract void eat();
}
	
	

