public class OverloadTest03 {
    public static void main(String[] args){
        //println() 是不是一个方法名
        //方法重载
        System.out.println(123);
        System.out.println("123");
        System.out.println(123.123);
        System.out.println('2');
        System.out.println(30.2F);
        System.out.println(100L);

    }
}
