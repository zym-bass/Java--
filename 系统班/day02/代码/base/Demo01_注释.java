package com.ujiuye.base;

// 单行注释 : 定义出一个类, 类名Demo01_注释
public class Demo01_注释 {
	/*
	 *  多行注释 :
	 *        定义出程序唯一执行入口, main方法
	 *   main方法中实现的功能就是输出打印 "你好"
	 *   // 我是嵌套的单行注释
	 */
	public static void main(String[] args) {
		System.out.println("你好");
		//System.out.println("你好吗?");
		System.out.println("你好不好?");
	}
}
